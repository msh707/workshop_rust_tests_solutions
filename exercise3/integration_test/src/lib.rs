pub mod calculator {
    pub fn addition(number1: i32, number2: i32) -> i32 {
        number1 + number2
    }

    pub fn subtraction(number1: i32, number2: i32) -> i32 {
        number1 - number2
    }

    pub fn multiplication(number1: i32, number2: i32) -> i32 {
        number1 * number2
    }

    pub fn division(number1: i32, number2: i32) -> i32 {
        number1 / number2
    }
}
